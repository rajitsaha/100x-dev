#!/usr/bin/env bash
set -e

REPO_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
WORKFLOWS_DIR="$REPO_DIR/workflows"
CLAUDE_DIR="$HOME/.claude"
COMMANDS_DIR="$CLAUDE_DIR/commands"
SETTINGS_FILE="$CLAUDE_DIR/settings.json"

GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
NC='\033[0m'

install_global() {
  echo ""
  echo "Installing workflows for Claude Code..."
  mkdir -p "$COMMANDS_DIR"

  if [ -d "$COMMANDS_DIR" ] && [ "$(ls -A "$COMMANDS_DIR" 2>/dev/null)" ]; then
    BACKUP="$CLAUDE_DIR/commands.bak.$(date +%Y%m%d_%H%M%S)"
    cp -r "$COMMANDS_DIR" "$BACKUP"
    echo -e "  ${YELLOW}→ Backed up existing commands to $(basename "$BACKUP")${NC}"
  fi

  count=0
  for f in "$WORKFLOWS_DIR/"*.md; do
    cp "$f" "$COMMANDS_DIR/$(basename "$f")"
    echo "" >> "$COMMANDS_DIR/$(basename "$f")"
    # shellcheck disable=SC2016
    echo '$ARGUMENTS' >> "$COMMANDS_DIR/$(basename "$f")"
    count=$((count + 1))
  done

  if [ -d "$WORKFLOWS_DIR/db-engines" ]; then
    mkdir -p "$COMMANDS_DIR/db-engines"
    for f in "$WORKFLOWS_DIR/db-engines/"*.md; do
      cp "$f" "$COMMANDS_DIR/db-engines/"
    done
    engine_count=$(find "$WORKFLOWS_DIR/db-engines/" -maxdepth 1 -name '*.md' 2>/dev/null | wc -l | tr -d ' ')
    echo -e "  ${GREEN}→ Copied db-engines/ ($engine_count engine files) ✓${NC}"
  fi

  echo -e "  ${GREEN}→ Copied $count workflows to ~/.claude/commands/ ✓${NC}"
  echo -e "  ${CYAN}→ Restart Claude Code to load new commands${NC}"
}

install_project() {
  local project_path="${1:-.}"
  local instruction_file=""

  # Check if any instruction file already exists
  for f in CLAUDE.md AGENTS.md .cursorrules .windsurfrules GEMINI.md; do
    if [ -f "$project_path/$f" ]; then
      instruction_file="$project_path/$f"
      break
    fi
  done
  if [ -f "$project_path/.github/copilot-instructions.md" ]; then
    instruction_file="$project_path/.github/copilot-instructions.md"
  fi

  if [ -n "$instruction_file" ]; then
    echo -e "  ${CYAN}→ Instruction file already exists: $instruction_file (skipping scaffold)${NC}"
    return
  fi

  local target="$project_path/CLAUDE.md"
  local project_name
  project_name="$(basename "$(cd "$project_path" && pwd)")"

  cat > "$target" << SCAFFOLD
# $project_name — Project Instructions

<!-- Generated by 100x-dev. Fill in the sections below so workflows like /db, /gate, /launch have project context. -->

## Project

<!-- Describe what this project does in 2-3 sentences. Used by /architect and /enterprise-design. -->
description: TODO

## Database

<!-- Used by /db and /query. Remove if not applicable. -->
# engine: postgres          # postgres | mysql | sqlite | snowflake | bigquery | athena | databricks | presto | oracle | cloud-sql
# connection: default       # name of the connection block below
# connections:
#   default:
#     host: localhost
#     port: 5432
#     name: mydb
#     user: myuser
#     auth: env:DB_PASSWORD  # env:VAR_NAME | secret:SECRET_NAME | prompt

## Cloud (GCP)

<!-- Used by /gate, /cloud-security, /launch. Remove if not on GCP. -->
# gcp_project: my-gcp-project
# cloud_run_service: my-service
# region: us-central1

## Production

<!-- Used by /launch and /push for health checks and smoke tests. -->
# production_url: https://example.com
# health_url: https://example.com/health

## Security Exceptions

<!-- Known findings to skip in /security scans. One per line. -->
# security_exceptions:
#   - CVE-2023-XXXX: false positive in test dependency

## Rules

<!-- Add project-specific rules for Claude here. /update-claude appends to this section. -->

SCAFFOLD

  echo -e "  ${GREEN}→ Scaffolded CLAUDE.md in $project_path ✓${NC}"
  echo -e "  ${YELLOW}→ Edit $target to fill in db/cloud/production config${NC}"
}

check_bun() {
  if ! command -v bun &>/dev/null; then
    echo ""
    echo -e "  ${YELLOW}⚠ Bun is required by the claude-mem plugin but is not installed.${NC}"
    echo ""
    read -rp "  Install Bun now? (recommended) [Y/n]: " install_bun
    install_bun="${install_bun:-Y}"
    if [[ "$install_bun" =~ ^[Yy]$ ]]; then
      echo "  Installing Bun..."
      curl -fsSL https://bun.sh/install | bash
      # Reload PATH so bun is available immediately
      export BUN_INSTALL="$HOME/.bun"
      export PATH="$BUN_INSTALL/bin:$PATH"
      if command -v bun &>/dev/null; then
        echo -e "  ${GREEN}→ Bun installed ✓${NC}"
      else
        echo -e "  ${YELLOW}→ Bun installed — restart your terminal to activate it${NC}"
      fi
    else
      echo -e "  ${YELLOW}→ Skipping Bun install. claude-mem hooks will show errors until Bun is available.${NC}"
    fi
  fi
}

install_plugins() {
  echo ""
  echo "Installing plugins for Claude Code..."
  mkdir -p "$CLAUDE_DIR"

  # claude-mem requires Bun — check before installing plugins
  check_bun

  PLUGINS_FILE="$REPO_DIR/plugins/plugins.json"

  new_plugins=$(python3 -c "
import json
with open('$PLUGINS_FILE') as f:
    data = json.load(f)
print(json.dumps(data.get('plugins', [])))
")

  extra_marketplaces=$(python3 -c "
import json
with open('$PLUGINS_FILE') as f:
    data = json.load(f)
print(json.dumps(data.get('extraKnownMarketplaces', {})))
")

  if [ ! -f "$SETTINGS_FILE" ]; then
    echo '{}' > "$SETTINGS_FILE"
  fi

  python3 << PYEOF
import json

with open('$SETTINGS_FILE', 'r') as f:
    settings = json.load(f)

new_plugins = $new_plugins
extra_marketplaces = $extra_marketplaces

enabled = settings.get('enabledPlugins', {})
added = 0
for p in new_plugins:
    if p not in enabled:
        enabled[p] = True
        added += 1

settings['enabledPlugins'] = enabled

existing_marketplaces = settings.get('extraKnownMarketplaces', {})
existing_marketplaces.update(extra_marketplaces)
settings['extraKnownMarketplaces'] = existing_marketplaces

with open('$SETTINGS_FILE', 'w') as f:
    json.dump(settings, f, indent=2)

print(f'  Added {added} new plugins ({len(enabled)} total)')
PYEOF

  echo -e "  ${GREEN}→ Plugins merged into ~/.claude/settings.json ✓${NC}"
  echo -e "  ${CYAN}→ Run /reload-plugins in Claude Code to activate${NC}"
}

if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
  case "${1:-}" in
    --plugins) install_plugins ;;
    *) install_global ;;
  esac
fi
